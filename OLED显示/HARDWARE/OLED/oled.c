//向SSD1306写一个字节
//dat:要写入的数据/命令
//cmd:数据/命令标志 1数据 0命令
void OLED_WR_Byte(u8 dat,u8 cmd)
{
	// 写时序
	DATAOUT(dat);	// 装入数据 （PC 0-7）
 	OLED_RS=cmd;	// 数据/命令
	OLED_CS=0;	  // 片选-拉低使能
	OLED_WR=0;	  // 写拉低-制造上升沿-开始
	OLED_WR=1;		// 写拉高-实现上升沿-写入数据完成
	// 置位-为下次做准备
	OLED_CS=1;	  // 置位-片选-拉高
	OLED_RS=1;	  // 置位-数据/命令位-拉高
} 	   

#else

//向SSD1306写入一个字节。
//dat:要写入的数据/命令
//cmd:数据/命令标志 0,表示命令;1,表示数据;
void OLED_WR_Byte(u8 dat,u8 cmd)
{	
	u8 i;			  
	OLED_RS=cmd; //写命令 
	OLED_CS=0;		  
	for(i=0;i<8;i++)
	{			  
		OLED_SCLK=0;
		if(dat&0x80)OLED_SDIN=1;
		else OLED_SDIN=0;
		OLED_SCLK=1;
		dat<<=1;   
	}				 
	OLED_CS=1;		  
	OLED_RS=1;   	  
} 

#endif

//更新显存到OLED		 
void OLED_Refresh_Gram(void)
{
	u8 i,n;		    
	for(i=0;i<8;i++)  		// 页循环
	{  
		OLED_WR_Byte (0xb0+i,OLED_CMD);    //设置页地址（0~7）
		OLED_WR_Byte (0x00,OLED_CMD);      //设置显示位置―列低地址
		OLED_WR_Byte (0x10,OLED_CMD);      //设置显示位置―列高地址 
		
		for(n=0;n<128;n++)	// 本页-列操作
			OLED_WR_Byte(OLED_GRAM[n][i],OLED_DATA); // 1:写数据
	}   
}
	  	  
//开启OLED显示-开启电荷泵  
void OLED_Display_On(void)
{
	OLED_WR_Byte(0X8D,OLED_CMD);  //SET DCDC命令
	OLED_WR_Byte(0X14,OLED_CMD);  //DCDC ON
	OLED_WR_Byte(0XAF,OLED_CMD);  //DISPLAY ON
}

//关闭OLED显示-关闭电荷泵    
void OLED_Display_Off(void)
{
	OLED_WR_Byte(0X8D,OLED_CMD);  //SET DCDC命令
	OLED_WR_Byte(0X10,OLED_CMD);  //DCDC OFF
	OLED_WR_Byte(0XAE,OLED_CMD);  //DISPLAY OFF
}		   

//清屏函数-屏幕变黑
void OLED_Clear(void)  
{  
	u8 i,n;  
	for(i=0;i<8;i++)					// 纵向-页循环
		for(n=0;n<128;n++)  		// 横向-列循环
			OLED_GRAM[n][i]=0X00; // 设置坐标值-熄灭
	
	//刷新OLED显示
	OLED_Refresh_Gram();
}

//画点 
//x:0~127 横坐标-点所在列
//y:0~63	纵坐标-点所在行
//t:1 填充 0,清空				   
void OLED_DrawPoint(u8 x,u8 y,u8 t)
{
	u8 pos,bx,temp=0;
	
	// 有效性校验
	if(x>127||y>63)
		return;
	
	pos = 7-y/8;			// 根据Y轴坐标计算点在第几页
	bx = y%8;					// 纵向偏移量-点所在页的Y轴坐标
	
	temp = 1<<(7-bx); // 得到这一位是哪一位  例如 ： 00000001
	
	//赋值点所在页的列所在bit位
	if(t)
		OLED_GRAM[x][pos]|=temp; // 置1-或上00000001 将最低位置1，其他位不影响
	else 
		OLED_GRAM[x][pos]&=~temp;// 置0-与上11111110 将最低位置0，其他位不影响
}

//x1,y1,x2,y2 填充区域的对角坐标
//确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63	 	 
//dot:0,清空;1,填充	  
void OLED_Fill(u8 x1,u8 y1,u8 x2,u8 y2,u8 dot)  
{  
	u8 x,y;  
	for(x=x1;x<=x2;x++)
	{
		for(y=y1;y<=y2;y++)
			OLED_DrawPoint(x,y,dot);// 画点
	}													    
	OLED_Refresh_Gram();//更新显示
}

//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示				 
//size:选择字体 12/16/24
void OLED_ShowChar(u8 x,u8 y,u8 chr,u8 size,u8 mode)
{      			    
	u8 temp,t,t1;
	u8 y0=y;
	u8 csize=(size/8+((size%8)?1:0))*(size/2);//得到字体一个字符对应点阵集所占的字节数
	chr=chr-' ';//得到偏移后的值-在字库码表中的位置
	
	for(t=0;t<csize;t++)
	{   
		if(size==12)  
			temp=asc2_1206[chr][t]; // 调用1206字体
		else if(size==16)
			temp=asc2_1608[chr][t];	// 调用1608字体
		else if(size==24)
			temp=asc2_2412[chr][t];	// 调用2412字体
		else 
			return;									// 没有的字库
		
		for(t1=0;t1<8;t1++)
		{
			if(temp&0x80)
				OLED_DrawPoint(x,y,mode);
			else 
				OLED_DrawPoint(x,y,!mode);
			
			temp<<=1;
			y++;
			
			if((y-y0)==size)
			{
				y=y0;
				x++;
				break;
			}
			
		}  	 
	}          
}

//m^n函数
u32 mypow(u8 m,u8 n)
{
	u32 result=1;	 
	while(n--)result*=m;    
	return result;
}	

//显示2个数字
//x,y :起点坐标	 
//len :数字的位数
//size:字体大小
//mode:模式	0,填充模式;1,叠加模式
//num:数值(0~4294967295);	 		  
void OLED_ShowNum(u8 x,u8 y,u32 num,u8 len,u8 size)
{         	
	u8 t,temp;
	u8 enshow=0;						   
	for(t=0;t<len;t++)
	{
		temp=(num/mypow(10,len-t-1))%10;
		if(enshow==0&&t<(len-1))
		{
			if(temp==0)
			{
				OLED_ShowChar(x+(size/2)*t,y,' ',size,1);
				continue;
			}else enshow=1; 
		 	 
		}
	 	OLED_ShowChar(x+(size/2)*t,y,temp+'0',size,1); 
	}
} 

//显示字符串
//x,y:起点坐标  
//size:字体大小 
//*p:字符串起始地址 
void OLED_ShowString(u8 x,u8 y,const u8 *p,u8 size)
{	
    while((*p<='~')&&(*p>=' '))//判断是不是非法字符!
    {       
        if(x>(128-(size/2))){x=0;y+=size;}
        if(y>(64-size)){y=x=0;OLED_Clear();}
        OLED_ShowChar(x,y,*p,size,1);	 
        x+=size/2;
        p++;
    }  
	
}	   

//初始化SSD1306					    
void OLED_Init(void)
{ 	
 
 	GPIO_InitTypeDef  GPIO_InitStructure;
 	
 	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC|RCC_APB2Periph_GPIOD|RCC_APB2Periph_GPIOG, ENABLE);	 //使能PC,D,G端口时钟

	// 初始化 PD3-RS：数据/命令 PD6-CS：片选
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_3|GPIO_Pin_6;	 // PD3,PD6推挽输出  
 	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 		   // 推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;			 // 速度50MHz
 	GPIO_Init(GPIOD, &GPIO_InitStructure);	  						 // 初始化GPIOD3,6
	
 	GPIO_SetBits(GPIOD,GPIO_Pin_3|GPIO_Pin_6);						 // PD3,PD6 输出高

 #if OLED_MODE==1	// 8080并口
 
#define OLED_CS  PDout(6)
#define OLED_RST PGout(15) 	
#define OLED_RS  PDout(3)
#define OLED_WR  PGout(14)		  
#define OLED_RD  PGout(13)	  

	// 初始化 PC0-7 -- 数据线
 	GPIO_InitStructure.GPIO_Pin = 0xFF; 		// PC0~7 OUT 推挽输出
 	GPIO_Init(GPIOC, &GPIO_InitStructure);
	
 	GPIO_SetBits(GPIOC,0xFF); 							// PC0~7输出高

	// 初始化 PG13-RD-读 ，PG14-WR-写 ，PG15-RST-复位
 	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_13|GPIO_Pin_14|GPIO_Pin_15; // PG13,14,15 OUT 推挽输出
 	GPIO_Init(GPIOG, &GPIO_InitStructure);
	
 	GPIO_SetBits(GPIOG,GPIO_Pin_13|GPIO_Pin_14|GPIO_Pin_15);					 // PG13,14,15 OUT 输出高

 #else
 
 	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_0|GPIO_Pin_1;	 // PC0,1 OUT 推挽输出
 	GPIO_Init(GPIOC, &GPIO_InitStructure); 
 	GPIO_SetBits(GPIOC,GPIO_Pin_0|GPIO_Pin_1);						 // PC0,1 OUT 输出高

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_15;				  	 // PG15 OUT 推挽输出 RST
 	GPIO_Init(GPIOG, &GPIO_InitStructure);
 	GPIO_SetBits(GPIOG,GPIO_Pin_15);						 					 // PG15 OUT 输出高

 #endif

	// 引脚置位-全部置高
	OLED_CS=1;
	OLED_RS=1;	 
	
	// 复位OLED
	OLED_RST=0;
	delay_ms(100);
	OLED_RST=1; 
					  
	//初始化OLED-来自OLED手册
	OLED_WR_Byte(0xAE,OLED_CMD); //关闭显示
	OLED_WR_Byte(0xD5,OLED_CMD); //设置时钟分频因子,震荡频率
	OLED_WR_Byte(80,OLED_CMD);   //[3:0],分频因子;[7:4],震荡频率
	OLED_WR_Byte(0xA8,OLED_CMD); //设置驱动路数
	OLED_WR_Byte(0X3F,OLED_CMD); //默认0X3F(1/64) 
	OLED_WR_Byte(0xD3,OLED_CMD); //设置显示偏移
	OLED_WR_Byte(0X00,OLED_CMD); //默认为0

	OLED_WR_Byte(0x40,OLED_CMD); //设置显示开始行 [5:0],行数.
													    
	OLED_WR_Byte(0x8D,OLED_CMD); //电荷泵设置
	OLED_WR_Byte(0x14,OLED_CMD); //bit2，开启/关闭
	OLED_WR_Byte(0x20,OLED_CMD); //设置内存地址模式
	OLED_WR_Byte(0x02,OLED_CMD); //[1:0],00，列地址模式;01，行地址模式;10,页地址模式;默认10;
	OLED_WR_Byte(0xA1,OLED_CMD); //段重定义设置,bit0:0,0->0;1,0->127;
	OLED_WR_Byte(0xC0,OLED_CMD); //设置COM扫描方向;bit3:0,普通模式;1,重定义模式 COM[N-1]->COM0;N:驱动路数
	OLED_WR_Byte(0xDA,OLED_CMD); //设置COM硬件引脚配置
	OLED_WR_Byte(0x12,OLED_CMD); //[5:4]配置
		 
	OLED_WR_Byte(0x81,OLED_CMD); //对比度设置
	OLED_WR_Byte(0xEF,OLED_CMD); //1~255;默认0X7F (亮度设置,越大越亮)
	OLED_WR_Byte(0xD9,OLED_CMD); //设置预充电周期
	OLED_WR_Byte(0xf1,OLED_CMD); //[3:0],PHASE 1;[7:4],PHASE 2;
	OLED_WR_Byte(0xDB,OLED_CMD); //设置VCOMH 电压倍率
	OLED_WR_Byte(0x30,OLED_CMD); //[6:4] 000,0.65*vcc;001,0.77*vcc;011,0.83*vcc;

	OLED_WR_Byte(0xA4,OLED_CMD); //全局显示开启;bit0:1,开启;0,关闭;(白屏/黑屏)
	OLED_WR_Byte(0xA6,OLED_CMD); //设置显示方式;bit0:1,反相显示;0,正常显示	    						   
	OLED_WR_Byte(0xAF,OLED_CMD); //开启显示	 
	
	// 清屏
	OLED_Clear();
}  





























